
fun main() {
    val a = 10
    val b = 5

    val isEqual = a == b
    val isNotEqual = a != b
    val isGreater = a > b
    val isLess = a < b
    val isGreaterOrEqual = a >= b
    val isLessOrEqual = a <= b

    println("isEqual: $isEqual")                   // Output: isEqual: false
    println("isNotEqual: $isNotEqual")             // Output: isNotEqual: true
    println("isGreater: $isGreater")               // Output: isGreater: true
    println("isLess: $isLess")                     // Output: isLess: false
    println("isGreaterOrEqual: $isGreaterOrEqual") // Output: isGreaterOrEqual: true
    println("isLessOrEqual: $isLessOrEqual")       // Output: isLessOrEqual: false

    
}

