
fun main() {
    var a = 5
    var b = 10

    a++    // Increment a by 1
    b--    // Decrement b by 1

    println("a: $a")  // Output: a: 6
    println("b: $b")  // Output: b: 9

    a += 3   // Equivalent to a = a + 3
    b -= 2   // Equivalent to b = b - 2

    println("a: $a")  // Output: a: 9
    println("b: $b")  // Output: b: 7

    a *= 2   // Equivalent to a = a * 2
    b /= 3   // Equivalent to b = b / 3
    var c = 7
    c %= 4   // Equivalent to c = c % 4

    println("a: $a")  // Output: a: 18
    println("b: $b")  // Output: b: 2
    println("c: $c")  // Output: c: 3
}



