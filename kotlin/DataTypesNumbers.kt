fun main(args: Array<String>) {

    /*
    Byte: It represents a 8-bit signed integer. The range of values that can be stored in a Byte variable is from -128 to 127.

    Short: It represents a 16-bit signed integer. The range of values that can be stored in a Short variable is from -32,768 to 32,767.

    Int: It represents a 32-bit signed integer. The range of values that can be stored in an Int variable is from -2,147,483,648 to 2,147,483,647.

    Long: It represents a 64-bit signed integer. The range of values that can be stored in a Long variable is from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807.

    Float: It represents a 32-bit floating-point number. Floats are used to store decimal numbers with a limited precision. They can represent a range of values, but their precision is limited compared to the Double type.

    Double: It represents a 64-bit floating-point number. Doubles are used to store decimal numbers with higher precision compared to Float.
        They can represent a wider range of values and are generally used for most floating-point calculations.


     */

    val myByte: Byte = 10
    val myShort: Short = 100
    val myInt: Int = 1000
    val myLong: Long = 100000L
    val myFloat: Float = 3.14F
    val myDouble: Double = 3.14159

    println("Byte: $myByte")
    println("Short: $myShort")
    println("Int: $myInt")
    println("Long: $myLong")
    println("Float: $myFloat")
    println("Double: $myDouble")



}